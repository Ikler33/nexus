//! Типизированная граница вызова инструмента (AGENT-1): [`Tool`] трейт + [`ToolSpec`] / [`ToolCall`] /
//! [`ToolError`].
//!
//! **R-1 (развязка слоёв):** типы границы НЕЙТРАЛЬНЫ к слоям и живут на верхнем уровне ядра — их
//! используют и агент (реестр/цикл, `crate::agent`), и актуатор (гейтнутые инструменты,
//! `crate::actuator::tools`), и AI-слой (tool-capable провайдер, `crate::ai::tools`) БЕЗ рёбер
//! actuator→agent / ai→agent. Старый путь `crate::agent::tool` сохранён реэкспортом.
//!
//! # Fail-closed на границе (I-4, ADR-009)
//! [`ToolCall::arguments`] — это **сырой JSON-текст**, как его вернула модель: НЕ пере-десериализованный
//! заранее. Каждый инструмент сам строго парсит свои аргументы (`serde` + `deny_unknown_fields`) и
//! возвращает [`ToolError::BadArgs`] при любом несоответствии. Граница НИКОГДА не «доугадывает»/коэрсит
//! кривые аргументы в валидные — лучше явный отказ (модель увидит ошибку и переспросит), чем тихое
//! исполнение не того.

use async_trait::async_trait;
use thiserror::Error;

/// Описание инструмента для запроса к tool-capable модели. `parameters` — JSON-Schema объекта
/// аргументов (как в OpenAI function-calling): провайдер заворачивает её в `tools[].function`.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ToolSpec {
    /// Дотированное имя инструмента — ключ реестра и `function.name` в запросе (напр. `"debug.echo"`).
    pub name: String,
    /// Человеко-/модель-читаемое назначение инструмента (`function.description`).
    pub description: String,
    /// JSON-Schema аргументов (`function.parameters`). Объект-схема; пустой `{}` — инструмент без аргументов.
    pub parameters: serde_json::Value,
}

/// Запрос модели на вызов инструмента (из распарсенного tool_calls стрима). `arguments` — СЫРОЙ JSON
/// (строка), не десериализован: строгий разбор делает сам инструмент (I-4 fail-closed).
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ToolCall {
    /// Идентификатор вызова от модели (коррелирует ToolCall↔ToolResult в потоке событий). Если модель
    /// не прислала id — цикл подставляет синтетический (см. провайдер), чтобы корреляция не ломалась.
    pub id: String,
    /// Имя вызываемого инструмента (ключ реестра).
    pub name: String,
    /// Сырые JSON-аргументы как строка (напр. `{"text":"hi"}`). Пустая строка трактуется как `{}`.
    pub arguments: String,
}

/// Ошибка границы инструмента. Доносится до цикла, который оборачивает её в `ToolResult{is_error:true}`
/// и скармливает модели как наблюдение (модель может восстановиться) — НЕ роняет весь прогон.
#[derive(Debug, Error, PartialEq, Eq)]
pub enum ToolError {
    /// В реестре нет инструмента с таким именем (модель «выдумала» инструмент). Fail-closed: не паника,
    /// не тихий no-op — возвращается как ошибочный результат.
    #[error("неизвестный инструмент: {0}")]
    UnknownTool(String),
    /// Аргументы не прошли строгий разбор инструмента (невалидный JSON / неизвестное поле / не тот тип).
    #[error("некорректные аргументы инструмента: {0}")]
    BadArgs(String),
    /// Сбой во время исполнения тела инструмента (для будущих инструментов с побочными эффектами;
    /// у безопасных стабов AGENT-1 не возникает).
    #[error("ошибка исполнения инструмента: {0}")]
    Exec(String),
}

/// Инструмент агента: объявляет себя ([`ToolSpec`]) и исполняется по сырым аргументам.
///
/// Object-safe (живёт за `Arc<dyn Tool>` в реестре), async через `#[async_trait]`. AGENT-1
/// реализуют ТОЛЬКО безопасные стабы (echo + read-only) — никакого fs/process/net (актуатор — AGENT-3).
#[async_trait]
pub trait Tool: Send + Sync {
    /// Статичное описание инструмента (имя/назначение/схема аргументов) — идёт в запрос к модели.
    fn spec(&self) -> ToolSpec;

    /// Исполнить инструмент по СЫРЫМ JSON-аргументам (строка). Возвращает текст результата (попадёт
    /// в промпт зафенсенным) либо [`ToolError`]. Инструмент сам строго разбирает `args` (I-4 fail-closed).
    async fn invoke(&self, args: &str) -> Result<String, ToolError>;
}
