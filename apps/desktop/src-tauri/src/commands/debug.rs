//! Журнал действий UI (режим отладки, запрос владельца 2026-06-11): фронт шлёт СОБЫТИЯ интерфейса
//! («открыл дайджест», «отправил вопрос в чат (web)»), бэкенд пишет их общим tracing-потоком —
//! в stdout и файловый журнал (`lib.rs::log_dir`). Зачем: отчёты вида «кликнул — ничего не
//! произошло» невоспроизводимы без следа; журнал показывает, дошло ли действие до обработчика.
//!
//! Приватность (принцип AC-SEC-6): только имена действий и метаданные (режим, длины, коды ошибок);
//! контент заметок/вопросов/статей сюда НЕ попадает — фронт-хелпер `logUi` это документирует.

/// Пишет UI-событие в журнал. Принимаем как есть (best-effort): это отладочный след, не телеметрия —
/// никуда не уходит, живёт в локальном файле.
#[tauri::command]
pub fn log_ui_event(event: String, detail: Option<String>) {
    tracing::info!(target: "ui", event = %event, detail = %detail.unwrap_or_default(), "ui");
}
