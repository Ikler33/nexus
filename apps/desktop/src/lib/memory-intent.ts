// MEM-5: распознавание ЯВНОЙ команды «сохрани это в память агента» в реплике пользователя.
// Явная команда = согласие (решение владельца): такой обмен сразу СОХРАНЯЕТ факт (без чипа-подтверждения,
// с тостом «Запомнил…» + «Отменить»). Без команды — прежнее авто-предложение чипом (D1, MEM-3).
// Лёгкая эвристика на фронте (без LLM) — мгновенно, до медленного извлечения факта.

// ВАЖНО: `\b` в JS — ASCII-only (кириллица не «слово» → границы не срабатывают). Поэтому границы слова
// строим вручную: слева не-буква (или начало), справа не-буква (или конец). Текст заранее в lowercase.
const W = 'a-zа-яё0-9_'; // символы «слова» с кириллицей (после toLowerCase)
const bL = `(?:^|[^${W}])`; // граница слева
const bR = `(?:[^${W}]|$)`; // граница справа

// Императивы «сохранить в память» (RU/EN). Анти-инъекция тут не нужна — это собственная реплика юзера.
const SAVE_PATTERNS: RegExp[] = [
  new RegExp(`${bL}запомни(?:те)?${bR}`), // «запомни/запомните» (НЕ «запоминай» — другая основа)
  new RegExp(`(?:сохрани|сохраните|добав[ьи]|добавьте|занеси|занесите|запиши|запишите)\\s+(?:это\\s+)?в\\s+памят`),
  new RegExp(`в\\s+памят[ьи]\\s+(?:сохрани|добав|занеси|запиши)`),
  new RegExp(`${bL}remember\\s+(?:that|this)${bR}`),
  new RegExp(`save\\s+(?:this\\s+|that\\s+|it\\s+)?to\\s+memory`),
  new RegExp(`keep\\s+in\\s+mind\\s+that${bR}`),
  new RegExp(`note\\s+to\\s+(?:self|memory)${bR}`),
];

// Отрицания — «не запомни/запоминай/сохрани/сохраняй/добавляй …», «don't remember» → НЕ команда.
// По СТЕМАМ (а не точным формам): «запомни» и «запоминай» оба после «не » → отказ (ревью MEM-5).
const NEGATIONS: RegExp[] = [
  new RegExp(`не\\s+(?:запом|сохран|добав|занос|занеси|запиш|надо\\s+запом)`),
  /don'?t\s+(?:remember|save)/,
  /no\s+need\s+to\s+remember/,
];

/**
 * Явно ли пользователь просит сохранить факт в память агента.
 * `false` при отрицании («не запоминай») и для «запоминай» (просьба запоминать впредь ≠ сохранить ЭТО).
 */
export function isExplicitSave(text: string): boolean {
  const t = text.trim().toLowerCase();
  if (!t) return false;
  if (NEGATIONS.some((re) => re.test(t))) return false;
  return SAVE_PATTERNS.some((re) => re.test(t));
}

// Конец команды — lookahead на разделитель/конец строки (НЕ `\b`: ASCII-only, после кириллицы не
// сработал бы). Далее съедаем сепараторы и связки «это/в память/that/что», сохраняя исходный регистр.
const STRIP_CMD =
  /^\s*(?:запомни(?:те)?|сохрани(?:те)?|добав[ьи](?:те)?|занеси(?:те)?|запиши(?:те)?|remember|save|note)(?=[\s:,]|$)[\s:,]*(?:это\s*)?(?:в\s+памят[ьи][\s:,]*)?(?:that\s+|this\s+|to\s+memory[\s:,]*)?(?:то,?\s*)?(?:что\s*)?/i;

/**
 * Грубый фолбэк: срезать командный префикс «запомни/сохрани в память …», оставив суть факта.
 * Применяется, если LLM-извлечение ничего не вернуло — чтобы явная команда не была no-op. Чистый
 * факт даёт модель; это лишь страховка. Режет до 140 символов.
 */
export function stripSaveCommand(text: string): string {
  return text.trim().replace(STRIP_CMD, '').trim().slice(0, 140);
}
