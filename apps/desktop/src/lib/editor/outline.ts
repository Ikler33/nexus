/**
 * EDIT-7: оглавление заметки. Чистое извлечение ATX-заголовков (`#`..`######`) из markdown
 * для панели Outline и перехода к секции. Заголовки ВНУТРИ огороженных код-блоков (``` / ~~~)
 * игнорируются — там `#` это комментарий/шебанг, не заголовок. Setext-заголовки (подчёркивание
 * `===`/`---` под строкой) — пост-v1 (редки и требуют lookahead на предыдущую строку).
 */

export interface Heading {
  /** Уровень 1–6 (число решёток). */
  level: number;
  /** Текст заголовка, очищенный от инлайн-разметки для показа в оглавлении. */
  text: string;
  /** 1-based номер исходной строки — совпадает с `node.position.start.line` remark (preview-jump). */
  line: number;
}

// ATX: до 3 пробелов отступа, 1–6 `#`, ОБЯЗАТЕЛЬНЫЙ пробел/таб, текст, опц. закрывающая
// последовательность ` #`+ (только ПОСЛЕ пробела — иначе `#` часть текста, как в `C#`).
const ATX_RE = /^[ \t]{0,3}(#{1,6})[ \t]+(.*?)(?:[ \t]+#+)?[ \t]*$/;
// Ограждение код-блока: до 3 пробелов отступа, затем ≥3 бэктика или тильды.
const FENCE_RE = /^[ \t]{0,3}(`{3,}|~{3,})/;

/**
 * Убирает базовую инлайн-разметку из текста заголовка для читаемого показа в оглавлении.
 * Двойные формы (`**`/`__`) снимаем первыми. У `_` соблюдаем правило CommonMark: подчёркивание
 * внутри слова НЕ эмфазис (`snake_case_name` остаётся как есть) — снимаем только на границах слова.
 * `*` по CommonMark может быть внутрисловным, поэтому снимается без оглядки на границы.
 */
export function cleanHeadingText(raw: string): string {
  return raw
    .replace(/`([^`]+)`/g, '$1') // `код` → код
    .replace(/!?\[\[([^\]|]+)(?:\|([^\]]+))?\]\]/g, (_m, tgt: string, alias?: string) => alias || tgt) // [[A|B]]→B, [[A]]→A
    .replace(/!?\[([^\]]*)\]\([^)]*\)/g, '$1') // [текст](url) → текст
    .replace(/\*\*(.+?)\*\*/g, '$1') // **жирный** → жирный
    .replace(/__(.+?)__/g, '$1') // __жирный__ → жирный
    .replace(/\*(.+?)\*/g, '$1') // *курсив* → курсив (внутрисловный `*` допустим в CommonMark)
    .replace(/(^|\W)_(.+?)_(?=\W|$)/g, '$1$2') // _курсив_ → курсив, но только на границах слова
    .replace(/~~(.+?)~~/g, '$1') // ~~зачёрк~~ → зачёрк
    .replace(/==(.+?)==/g, '$1') // ==хайлайт== → хайлайт
    .trim();
}

/**
 * Извлекает ATX-заголовки вне код-блоков, в порядке появления. Заголовки, текст которых после
 * очистки разметки пуст (`# ` / `## ****`), пропускаются — в оглавлении пустая строка бесполезна.
 */
export function extractHeadings(doc: string): Heading[] {
  const out: Heading[] = [];
  const lines = doc.split('\n');
  let fence: string | null = null; // символ открытого ограждения (` или ~); null — вне блока
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const f = FENCE_RE.exec(line);
    if (f) {
      const marker = f[1][0];
      if (fence === null) fence = marker;
      else if (fence === marker) fence = null; // закрывает только тем же символом
      continue;
    }
    if (fence !== null) continue;
    const m = ATX_RE.exec(line);
    if (m) {
      const text = cleanHeadingText(m[2]);
      if (text) out.push({ level: m[1].length, text, line: i + 1 });
    }
  }
  return out;
}
