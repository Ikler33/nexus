/**
 * Hermes-8 фикс: «чистый редакционный вид» заголовков. Удаляет ЭМОДЗИ из текста заголовка (шаблон
 * daily даёт `# 📅 …`, `## 🧠 …`/`## 💡 …`) — рендер заголовков типографикой Cormorant без эмодзи.
 * Применяется ТОЛЬКО к заголовкам (H1 masthead, H2-секции, H3–H6, оглавление): тело абзацев, эмодзи
 * приоритетов задач (`lib/editor/format.ts` PRIO_EMOJI), callout-иконки и теги НЕ затрагиваются. Это
 * display/render-трансформа — исходный `.md` не мутируется.
 *
 * ВАЖНО (adversarial FIX 2): НЕ режем text-презентационные символы ™©®ℹ↗✔⚙▶ и т.п. — в продуктовых/
 * юридических заметках это ТЕКСТ, не декор. Их кодпоинты лежат НИЖЕ U+1F000 и идут БЕЗ VS-16, поэтому
 * не попадают под эвристику. Стрипаем кодпоинт, только если он:
 *  - в supplementary-эмодзи-планах `[\u{1F000}-\u{1FAFF}]` (туда входят daily 📅🧠💡 = U+1F4C5/1F9E0/
 *    1F4A1 и региональные индикаторы U+1F1E6–1F1FF для флагов);
 *  - ЛЮБОЙ символ ПЕРЕД VS-16 `️` (U+FE0F, emoji-презентация) — стрипаем символ+VS16 (`⚙️`→∅, но `⚙`→`⚙`);
 *  - ZWJ `‍` (U+200D, склейки emoji-последовательностей — семьи/флаги) и одиночный VS-16/keycap-combiner.
 */

const VS16 = '\u{FE0F}'; // variation selector-16 (emoji-презентация)
const ZWJ = '\u{200D}'; // zero-width joiner (склейка emoji-последовательностей)
const KEYCAP = '\u{20E3}'; // combining enclosing keycap (хвост `1️⃣`)
// Порядок альтернатив: «база+VS16» ПЕРВОЙ (жадно съедает символ перед VS16, в т.ч. ™©® с emoji-вариантом),
// затем supplementary-эмодзи-план, затем одиночные склейки/комбайнеры. `.` без флага `s` не пересекает `\n`.
const HEADING_EMOJI_RE = new RegExp(
  `.${VS16}|[\\u{1F000}-\\u{1FAFF}]|${ZWJ}|${VS16}|${KEYCAP}`,
  'gu',
);

/**
 * Поузельно-БЕЗОПАСНОЕ удаление эмодзи (adversarial FIX 1): только вырезает эмодзи-кодпоинты, БЕЗ
 * trim/collapse пробелов. remark-плагин зовёт это для КАЖДОГО text-ребёнка заголовка — схлопывание
 * двойных пробелов или trim ВНУТРИ узла склеило бы граничные пробелы между inline-узлами
 * (`Раздел `+strong+` и B` → `РазделAи B`). Границы заголовка чистит сам плагин (на уровне всего узла).
 */
export function removeHeadingEmoji(s: string): string {
  if (!s) return s;
  return s.replace(HEADING_EMOJI_RE, '');
}

/**
 * Полная очистка ЦЕЛЬНОЙ строки-заголовка: удалить эмодзи + схлопнуть сдвоенные пробелы (эмодзи между
 * словами не оставляет двойного зазора) + `.trim()` (ведущий «📅 » → чистый текст). Зовётся ТОЛЬКО для
 * цельных строк — masthead-title (одна строка) и оглавление (один текст-заголовок), НЕ поузельно.
 */
export function stripHeadingEmoji(s: string): string {
  if (!s) return s;
  return removeHeadingEmoji(s).replace(/ {2,}/g, ' ').trim();
}
